    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        if ($request->title == null || $request->title == ""){
            return redirect("/admin/subject/add")->with("message", "Subject cannot be empty");
        }

        $subject = Category::create([
            "title" => $request->title,
        ]);


        if ($subject){
            QuestionGroup::create([
                "title" => "Select",
                "category_id" => $subject->id
            ]);
            return redirect("/admin/subjects/")->with("message", "New subject added");
        }else{
            return redirect("/admin/subject/add")->with("message", "An error occur");
        }

    }

